// --- Field filtering includes short_id ---

func TestShortID_AlwaysIncludedInFieldSelection(t *testing.T) {
	fields, err := service.ParseFields("provider_name,amount")
	if err != nil {
		t.Fatalf("ParseFields: %v", err)
	}
	if !fields["short_id"] {
		t.Fatal("expected short_id to be always included in parsed fields")
	}
	if !fields["id"] {
		t.Fatal("expected id to be always included in parsed fields")
	}
}

func TestShortID_FilterTransactionFields(t *testing.T) {
	txn := service.TransactionResponse{
		ID:           "some-uuid",
		ShortID:      "Ab12Cd34",
		ProviderName: "Coffee",
		Amount:       4.50,
	}

	fields := map[string]bool{
		"id":            true,
		"short_id":      true,
		"provider_name": true,
	}

	filtered := service.FilterTransactionFields(txn, fields)
	if filtered["short_id"] != "Ab12Cd34" {
		t.Fatalf("expected short_id in filtered output, got %v", filtered["short_id"])
	}
	if filtered["id"] != "some-uuid" {
		t.Fatalf("expected id in filtered output, got %v", filtered["id"])
	}
}

