// --- SetTransactionCategory with short IDs ---

func TestShortID_SetTransactionCategory_WithShortIDs(t *testing.T) {
	svc, queries, pool := newService(t)
	ctx := context.Background()

	user := testutil.MustCreateUser(t, queries, "Alice")
	conn := testutil.MustCreateConnection(t, queries, user.ID, "sid_cat_conn")
	acct := testutil.MustCreateAccount(t, queries, conn.ID, "sid_cat_acct", "Checking")
	dbTxn := testutil.MustCreateTransaction(t, queries, acct.ID, "sid_cat_txn", "Restaurant", 3500, "2025-04-01")

	// Create a category.
	var catID pgtype.UUID
	var catShortID string
	err := pool.QueryRow(ctx,
		"INSERT INTO categories (slug, display_name) VALUES ('sid_test_food', 'Food') RETURNING id, short_id",
	).Scan(&catID, &catShortID)
	if err != nil {
		t.Fatalf("insert category: %v", err)
	}

	// Categorize using short_ids for both transaction and category.
	err = svc.SetTransactionCategory(ctx, dbTxn.ShortID, catShortID)
	if err != nil {
		t.Fatalf("SetTransactionCategory with short_ids: %v", err)
	}

	// Verify it worked.
	txn, err := svc.GetTransaction(ctx, dbTxn.ShortID)
	if err != nil {
		t.Fatalf("GetTransaction: %v", err)
	}
	if txn.Category == nil {
		t.Fatal("expected category to be set")
	}
	if *txn.Category.Slug != "sid_test_food" {
		t.Fatalf("expected category slug 'sid_test_food', got %q", *txn.Category.Slug)
	}
}
