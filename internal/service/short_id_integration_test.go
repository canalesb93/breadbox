func TestShortID_Resolver_ListTransactions_FilterByUserShortID(t *testing.T) {
	svc, queries, _ := newService(t)
	ctx := context.Background()

	alice := testutil.MustCreateUser(t, queries, "Alice")
	bob := testutil.MustCreateUser(t, queries, "Bob")
	connA := testutil.MustCreateConnection(t, queries, alice.ID, "sid_filter_connA")
	connB := testutil.MustCreateConnection(t, queries, bob.ID, "sid_filter_connB")
	acctA := testutil.MustCreateAccount(t, queries, connA.ID, "sid_filter_acctA", "Alice Checking")
	acctB := testutil.MustCreateAccount(t, queries, connB.ID, "sid_filter_acctB", "Bob Checking")
	testutil.MustCreateTransaction(t, queries, acctA.ID, "sid_filter_txnA", "Alice Purchase", 1000, "2025-03-15")
	testutil.MustCreateTransaction(t, queries, acctB.ID, "sid_filter_txnB", "Bob Purchase", 2000, "2025-03-15")

	// Filter by Alice's short_id
	aliceShortID := alice.ShortID
	result, err := svc.ListTransactions(ctx, service.TransactionListParams{
		UserID: &aliceShortID,
		Limit:  50,
	})
	if err != nil {
		t.Fatalf("ListTransactions with user short_id: %v", err)
	}
	if len(result.Transactions) != 1 {
		t.Fatalf("expected 1 transaction for Alice, got %d", len(result.Transactions))
	}
	if result.Transactions[0].ProviderName != "Alice Purchase" {
		t.Fatalf("expected 'Alice Purchase', got %q", result.Transactions[0].ProviderName)
	}
}

